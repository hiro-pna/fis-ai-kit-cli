/**
 * fis init — Scaffold artifact directories for a new project.
 */

import { mkdirSync, existsSync, writeFileSync } from 'node:fs'
import { join } from 'node:path'

const ARTIFACT_DIRS = [
  'artifacts/prd',
  'artifacts/trd',
  'artifacts/epics',
  'artifacts/stories',
  'artifacts/test-specs',
  'artifacts/tsd',
  'artifacts/plans',
  'artifacts/bugs',
  'artifacts/change-requests',
  'artifacts/personas',
  'artifacts/reviews',
  'artifacts/reports',
  'artifacts/visuals',
  '.fis/logs',
]

const README_CONTENT = `# Artifacts

Generated by fis-cli. Each artifact lives in its dedicated directory:

| Directory | Type | Producer Skill (auto-activated by Claude on natural prompt) |
|---|---|---|
| prd/ | Product Requirements Document | create-prd (BA) |
| trd/ | Technical Requirements Document | design-trd (SA) |
| epics/ | Epic groupings | decompose-epic (SA) |
| stories/ | User Stories with AC | write-story (SA) |
| test-specs/ | Test Specifications | write-test-spec (QA) |
| tsd/ | Test Strategy Document (project-level) | design-tsd (QA) |
| plans/ | Implementation Plans (light) | plan-impl (DEV) |
| bugs/ | Bug reports (light) | bug-triage (QA) |
| change-requests/ | Change Requests (light) | change-request (BA) |
| personas/ | Stakeholder + Three Amigos personas | manage-personas (BA) |
| reviews/ | Three Amigos review reports | three-amigos review-* |
| reports/ | Drift, scout, sa-review, code-review | (auto-generated) |
| visuals/ | Diagrams, screenshots, mockups | (manual + skills) |

DO NOT delete \`.fis/logs/\` — used for handoff-gate audit log.
`

export async function initCommand(args: string[]): Promise<void> {
  let target = process.cwd()
  for (let i = 0; i < args.length; i++) {
    if (args[i] === '--target' && args[i + 1]) {
      target = args[i + 1]
      i++
    }
  }

  console.log(`Initializing FIS AI Kit artifacts at: ${target}`)

  let created = 0
  let skipped = 0
  for (const dir of ARTIFACT_DIRS) {
    const full = join(target, dir)
    if (existsSync(full)) {
      skipped++
    } else {
      mkdirSync(full, { recursive: true })
      created++
    }
  }

  // Create README in artifacts/
  const readmePath = join(target, 'artifacts', 'README.md')
  if (!existsSync(readmePath)) {
    writeFileSync(readmePath, README_CONTENT)
  }

  // Create .fisrc.json with project config
  const configPath = join(target, '.fisrc.json')
  if (!existsSync(configPath)) {
    const config = {
      version: '0.1.0',
      project: {
        name: target.split('/').pop() || 'unknown',
        created: new Date().toISOString().slice(0, 10),
      },
      handoff_gate: {
        enabled: true,
      },
      three_amigos: {
        async_sla_hours: 24,
      },
    }
    writeFileSync(configPath, JSON.stringify(config, null, 2))
  }

  console.log(`✅ Created ${created} directories, skipped ${skipped} existing.`)
  console.log(`Next step: fis install (copy claude/ + templates/ into project)`)
}
